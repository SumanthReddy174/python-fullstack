# Dictionary with tuples
person_info = {
    "Alice": ("New York", 25),
    "Bob": ("London", 30),
    "Charlie": ("Paris", 28)
}

# Print the dictionary
print(person_info)

# Access a tuple
print("Bob's info:", person_info["Bob"])

# Access an item inside the tuple
print("Charlie's city:", person_info["Charlie"][0])
